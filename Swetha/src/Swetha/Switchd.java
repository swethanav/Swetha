package Swetha;

public class Switchd {

	public static void main(String[] args) {
		int num=20;
		 switch (num) {
		 case 1:System.out.println("It is 5");
		        break;
		 case 2:System.out.println("It is 10");
		        break;
		 case 3:System.out.println("It is 15");
		        break;
		 case 20:System.out.println("It is 20");
		        break;
		 default:System.out.println("It is not present");
		 }

	}

}
